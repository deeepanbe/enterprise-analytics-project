    SELECT 
        s.SELLER_ID,
        s.SELLER_CITY,
        s.SELLER_STATE,
        COUNT(DISTINCT o.ORDER_ID) as TOTAL_ORDERS,
        ROUND(SUM(oi.PRICE), 2) as TOTAL_REVENUE,
        ROUND(AVG(DATEDIFF('day', o.ORDER_ESTIMATED_DELIVERY_DATE, o.ORDER_DELIVERED_CUSTOMER_DATE)), 2) as AVG_DELAY,
        ROUND(AVG(r.REVIEW_SCORE), 2) as AVG_RATING,
        COUNT(DISTINCT oi.PRODUCT_ID) as UNIQUE_PRODUCTS,
        COUNT(DISTINCT o.CUSTOMER_ID) as UNIQUE_CUSTOMERS
    FROM OLIST_E_COMMERCE.PUBLIC.V_ORDER_ITEMS oi
    JOIN OLIST_E_COMMERCE.PUBLIC.V_ORDERS o ON oi.ORDER_ID = o.ORDER_ID
    JOIN OLIST_E_COMMERCE.PUBLIC.V_SELLERS s ON oi.SELLER_ID = s.SELLER_ID
    LEFT JOIN OLIST_E_COMMERCE.PUBLIC.V_REVIEWS r ON o.ORDER_ID = r.ORDER_ID
    WHERE o.ORDER_STATUS = 'delivered' AND o.ORDER_DELIVERED_CUSTOMER_DATE IS NOT NULL
    GROUP BY 1, 2, 3
    HAVING COUNT(DISTINCT o.ORDER_ID) >= 50
)
SELECT 
    *,
    ROUND((AVG_RATING * 20) - (ABS(AVG_DELAY) * 2) + (TOTAL_ORDERS / 100), 2) as SELLER_PERFORMANCE_SCORE
FROM seller_metrics
ORDER BY SELLER_PERFORMANCE_SCORE DESC
LIMIT 20;

