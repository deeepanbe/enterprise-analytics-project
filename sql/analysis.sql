-- Q28: Customer Lifetime Value Segmentation (RFM Analysis)
WITH rfm_base AS (
    SELECT 
        o.CUSTOMER_ID,
        MAX(ORDER_PURCHASE_TIMESTAMP) as LAST_ORDER_DATE,
        COUNT(DISTINCT o.ORDER_ID) as FREQUENCY,
        SUM(PAYMENT_VALUE) as MONETARY
    FROM OLIST_E_COMMERCE.PUBLIC.V_ORDERS o
    JOIN OLIST_E_COMMERCE.PUBLIC.V_PAYMENTS p ON o.ORDER_ID = p.ORDER_ID
    WHERE o.ORDER_STATUS = 'delivered'
    GROUP BY 1
),
rfm_calc AS (
    SELECT 
        CUSTOMER_ID,
        DATEDIFF('day', LAST_ORDER_DATE, (SELECT MAX(ORDER_PURCHASE_TIMESTAMP) FROM OLIST_E_COMMERCE.PUBLIC.V_ORDERS)) as RECENCY,
        FREQUENCY,
        MONETARY,
        NTILE(4) OVER (ORDER BY RECENCY DESC) as R_SCORE,
        NTILE(4) OVER (ORDER BY FREQUENCY ASC) as F_SCORE,
        NTILE(4) OVER (ORDER BY MONETARY ASC) as M_SCORE
    FROM rfm_base
)
SELECT 
    CONCAT(R_SCORE, F_SCORE, M_SCORE) as RFM_SEGMENT,
    CASE 
        WHEN R_SCORE >= 3 AND F_SCORE >= 3 AND M_SCORE >= 3 THEN 'Champions'
        WHEN R_SCORE >= 3 AND F_SCORE >= 2 THEN 'Loyal Customers'
        WHEN R_SCORE >= 3 AND M_SCORE >= 3 THEN 'Big Spenders'
        WHEN R_SCORE <= 2 AND F_SCORE <= 2 AND M_SCORE <= 2 THEN 'At Risk'
        WHEN R_SCORE <= 2 THEN 'Lost'
        ELSE 'Regular'
    END as CUSTOMER_TIER,
    COUNT(*) as CUSTOMER_COUNT,
    ROUND(AVG(RECENCY), 0) as AVG_RECENCY,
    ROUND(AVG(FREQUENCY), 1) as AVG_FREQUENCY,
    ROUND(AVG(MONETARY), 2) as AVG_MONETARY
FROM rfm_calc
GROUP BY 1, 2
ORDER BY AVG_MONETARY DESC;

