-- Q12: New vs Repeat Customers (by year)
WITH customer_orders AS (
    SELECT 
        CUSTOMER_UNIQUE_ID,
        YEAR(ORDER_PURCHASE_TIMESTAMP) as order_year,
        COUNT(DISTINCT o.ORDER_ID) as orders_per_year
    FROM OLIST_E_COMMERCE.PUBLIC.V_ORDERS o
    JOIN OLIST_E_COMMERCE.PUBLIC.V_CUSTOMERS c ON o.CUSTOMER_ID = c.CUSTOMER_ID
    GROUP BY 1, 2
)
SELECT 
    order_year,
    COUNT(DISTINCT CUSTOMER_UNIQUE_ID) as TOTAL_CUSTOMERS,
    SUM(CASE WHEN orders_per_year = 1 THEN 1 ELSE 0 END) as NEW_CUSTOMERS,
    SUM(CASE WHEN orders_per_year > 1 THEN 1 ELSE 0 END) as REPEAT_CUSTOMERS,
    ROUND(SUM(CASE WHEN orders_per_year > 1 THEN 1 ELSE 0 END) * 100.0 / COUNT(DISTINCT CUSTOMER_UNIQUE_ID), 2) as REPEAT_RATE_PCT
FROM customer_orders
GROUP BY 1
ORDER BY 1;

-- Q13: Customer Retention Rate (Cohort Analysis)
WITH first_order AS (
    SELECT 
        CUSTOMER_UNIQUE_ID,
        MIN(ORDER_PURCHASE_TIMESTAMP) as FIRST_ORDER_DATE
    FROM OLIST_E_COMMERCE.PUBLIC.V_ORDERS o
    JOIN OLIST_E_COMMERCE.PUBLIC.V_CUSTOMERS c ON o.CUSTOMER_ID = c.CUSTOMER_ID
    GROUP BY 1
),
cohort AS (
    SELECT 
        CUSTOMER_UNIQUE_ID,
        DATE_TRUNC('month', FIRST_ORDER_DATE) as COHORT_MONTH,
        DATEDIFF('month', FIRST_ORDER_DATE, ORDER_PURCHASE_TIMESTAMP) as MONTHS_SINCE_FIRST
    FROM first_order f
    JOIN OLIST_E_COMMERCE.PUBLIC.V_ORDERS o ON f.CUSTOMER_UNIQUE_ID = o.CUSTOMER_ID
)
SELECT 
    COHORT_MONTH,
    COUNT(DISTINCT CUSTOMER_UNIQUE_ID) as TOTAL_CUSTOMERS,
    COUNT(DISTINCT CASE WHEN MONTHS_SINCE_FIRST = 1 THEN CUSTOMER_UNIQUE_ID END) as MONTH_1_RETAINED,
    ROUND(COUNT(DISTINCT CASE WHEN MONTHS_SINCE_FIRST = 1 THEN CUSTOMER_UNIQUE_ID END) * 100.0 / COUNT(DISTINCT CUSTOMER_UNIQUE_ID), 2) as RETENTION_RATE_PCT
FROM cohort
GROUP BY 1
ORDER BY 1
LIMIT 12;

