-- Q26: Rating Impact on Repeat Purchases (Using Window Functions)
WITH customer_ratings AS (
    SELECT 
        o.CUSTOMER_ID,
        r.REVIEW_SCORE,
        r.REVIEW_CREATION_DATE,
        LAG(r.REVIEW_SCORE) OVER (PARTITION BY o.CUSTOMER_ID ORDER BY r.REVIEW_CREATION_DATE) as PREV_RATING,
        COUNT(DISTINCT o.ORDER_ID) OVER (PARTITION BY o.CUSTOMER_ID) as TOTAL_CUSTOMER_ORDERS
    FROM OLIST_E_COMMERCE.PUBLIC.V_REVIEWS r
    JOIN OLIST_E_COMMERCE.PUBLIC.V_ORDERS o ON r.ORDER_ID = o.ORDER_ID
)
SELECT 
    CASE WHEN PREV_RATING >= 4 THEN 'High Rating Customer' ELSE 'Low Rating Customer' END as CUSTOMER_SEGMENT,
    AVG(REVIEW_SCORE) as AVG_RATING,
    AVG(TOTAL_CUSTOMER_ORDERS) as AVG_ORDERS,
    COUNT(DISTINCT CUSTOMER_ID) as CUSTOMER_COUNT
FROM customer_ratings
WHERE PREV_RATING IS NOT NULL
GROUP BY 1
ORDER BY AVG_RATING DESC;

