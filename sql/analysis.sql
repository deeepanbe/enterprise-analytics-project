-- Q21: Delivery Delay by Seller (Top performers and worst)
WITH seller_delivery AS (
    SELECT 
        s.SELLER_ID,
        s.SELLER_CITY,
        s.SELLER_STATE,
        COUNT(DISTINCT o.ORDER_ID) as TOTAL_ORDERS,
        ROUND(AVG(DATEDIFF('day', ORDER_ESTIMATED_DELIVERY_DATE, ORDER_DELIVERED_CUSTOMER_DATE)), 2) as AVG_DELAY_DAYS,
        SUM(CASE WHEN ORDER_DELIVERED_CUSTOMER_DATE > ORDER_ESTIMATED_DELIVERY_DATE THEN 1 ELSE 0 END) as LATE_COUNT,
        ROUND(SUM(CASE WHEN ORDER_DELIVERED_CUSTOMER_DATE > ORDER_ESTIMATED_DELIVERY_DATE THEN 1 ELSE 0 END) * 100.0 / COUNT(DISTINCT o.ORDER_ID), 2) as LATE_PCT
    FROM OLIST_E_COMMERCE.PUBLIC.V_ORDER_ITEMS oi
