-- ============================================================================

-- Create the final denormalized dashboard view for Power BI
CREATE OR REPLACE VIEW OLIST_E_COMMERCE.PUBLIC.FINAL_DASHBOARD AS
SELECT 
    o.ORDER_ID,
    o.ORDER_STATUS,
    o.ORDER_PURCHASE_TIMESTAMP,
    DATE_TRUNC('month', o.ORDER_PURCHASE_TIMESTAMP) as ORDER_MONTH,
    DATE_TRUNC('quarter', o.ORDER_PURCHASE_TIMESTAMP) as ORDER_QUARTER,
    YEAR(o.ORDER_PURCHASE_TIMESTAMP) as ORDER_YEAR,
    DATE_TRUNC('week', o.ORDER_PURCHASE_TIMESTAMP) as ORDER_WEEK,
    o.ORDER_APPROVED_AT,
    o.ORDER_DELIVERED_CARRIER_DATE,
    o.ORDER_DELIVERED_CUSTOMER_DATE,
    o.ORDER_ESTIMATED_DELIVERY_DATE,
    
    -- Delivery Metrics
    DATEDIFF('day', o.ORDER_PURCHASE_TIMESTAMP, o.ORDER_DELIVERED_CUSTOMER_DATE) as DELIVERY_DAYS,
    DATEDIFF('day', o.ORDER_ESTIMATED_DELIVERY_DATE, o.ORDER_DELIVERED_CUSTOMER_DATE) as DELAY_DAYS,
    CASE 
        WHEN o.ORDER_DELIVERED_CUSTOMER_DATE IS NULL THEN 'Not Delivered'
        WHEN o.ORDER_DELIVERED_CUSTOMER_DATE <= o.ORDER_ESTIMATED_DELIVERY_DATE THEN 'On Time'
        ELSE 'Late'
    END as DELIVERY_STATUS,
    
    -- Customer Dimension
    c.CUSTOMER_ID,
    c.CUSTOMER_UNIQUE_ID,
    c.CUSTOMER_CITY,
    c.CUSTOMER_STATE,
    c.CUSTOMER_ZIP_CODE_PREFIX,
    
    -- Order Items (Fact)
    oi.ORDER_ITEM_ID,
    oi.PRODUCT_ID,
    oi.SELLER_ID,
    oi.SHIPPING_LIMIT_DATE,
    oi.PRICE as ITEM_PRICE,
    oi.FREIGHT_VALUE,
    oi.PRICE + oi.FREIGHT_VALUE as TOTAL_ITEM_VALUE,
    
    -- Product Dimension
    p.PRODUCT_CATEGORY_NAME as CATEGORY_PORTUGUESE,
    COALESCE(ct.PRODUCT_CATEGORY_NAME_ENGLISH, p.PRODUCT_CATEGORY_NAME) as CATEGORY,
    p.PRODUCT_NAME_LENGTH,
    p.PRODUCT_DESCRIPTION_LENGTH,
    p.PRODUCT_PHOTOS_QTY,
    p.PRODUCT_WEIGHT_G,
    p.PRODUCT_LENGTH_CM,
    p.PRODUCT_HEIGHT_CM,
    p.PRODUCT_WIDTH_CM,
    
    -- Seller Dimension
    s.SELLER_CITY as SELLER_CITY,
    s.SELLER_STATE as SELLER_STATE,
    
    -- Payment Info (aggregated per order)
    pmt.PAYMENT_TYPE,
    pmt.PAYMENT_INSTALLMENTS,
    pmt.PAYMENT_VALUE,
    
    -- Review Info (aggregated per order)
    r.REVIEW_ID,
    r.REVIEW_SCORE,
    r.REVIEW_CREATION_DATE,
    r.REVIEW_COMMENT_TITLE,
    r.REVIEW_COMMENT_MESSAGE

FROM OLIST_E_COMMERCE.PUBLIC.V_ORDER_ITEMS oi
JOIN OLIST_E_COMMERCE.PUBLIC.V_ORDERS o ON oi.ORDER_ID = o.ORDER_ID
JOIN OLIST_E_COMMERCE.PUBLIC.V_CUSTOMERS c ON o.CUSTOMER_ID = c.CUSTOMER_ID
JOIN OLIST_E_COMMERCE.PUBLIC.V_PRODUCTS p ON oi.PRODUCT_ID = p.PRODUCT_ID
JOIN OLIST_E_COMMERCE.PUBLIC.V_SELLERS s ON oi.SELLER_ID = s.SELLER_ID
LEFT JOIN OLIST_E_COMMERCE.PUBLIC.V_CATEGORY_TRANSLATION ct ON p.PRODUCT_CATEGORY_NAME = ct.PRODUCT_CATEGORY_NAME
LEFT JOIN OLIST_E_COMMERCE.PUBLIC.V_PAYMENTS pmt ON o.ORDER_ID = pmt.ORDER_ID
LEFT JOIN OLIST_E_COMMERCE.PUBLIC.V_REVIEWS r ON o.ORDER_ID = r.ORDER_ID;

